// Basic Arithmetic Operations

let firstNumber = 5;
let secondNumber = 15;

console.log(firstNumber + secondNumber);
console.log(secondNumber - firstNumber);
console.log(firstNumber * secondNumber);
console.log(secondNumber / firstNumber);
console.log("-------------------------------");

// Working with Strings

let sentence = "I am learning JavaScript.";

console.log(sentence.length);
console.log(sentence.charAt(0));
console.log(sentence.charAt(sentence.length - 1));
console.log("-------------------------------");

//Math Object
let negativeNumber = -100;

console.log(Math.sqrt(negativeNumber));
console.log( Math.pow(negativeNumber, 2));
console.log(Math.abs(negativeNumber))
console.log("-------------------------------");

// Boolena Logic and Comparison Operators
let newFirstNumber = 67;
let newSecondNumber = 41;

console.log(newFirstNumber > newSecondNumber);
console.log(newFirstNumber < newSecondNumber);
console.log(newFirstNumber == newSecondNumber);
console.log("-------------------------------");

// Logical Operators
let hasSiblings = false;
let hasPets = true;

console.log(hasSiblings && hasPets);
console.log(hasSiblings || hasPets);
console.log(!hasSiblings);
console.log(!hasPets);
console.log("-------------------------------");

